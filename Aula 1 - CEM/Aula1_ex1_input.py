# Pegando os valores com INT para transforma-los em números inteiros
numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))

# Realizando os processos de contas
soma = numero1 + numero2
subtracao = numero1 - numero2

# Imprimindo os resultados utilizando o F - STRING
print(f"Esse é o valor da SOMA: {soma}")
print(f"Esse é o valor da SUBTRAÇÃO: {subtracao}")